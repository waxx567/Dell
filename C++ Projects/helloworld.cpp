#include <iostream>

int main() {
    
    // type conversion = converting one data type to another
    //                   implicit = automatic
    //                   explicit = precede value with new data type

    double x = (int) 3.14; // x = 3
    int y = int(3.14); // 3.14 -> 3
    char c = 100; // c = d
    int z = 'd'; // z = 100

    return 0;
}
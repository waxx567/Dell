#include <iostream>

int main() {

    // for loop = executes a block of code a specified number of times
    //            can use a counter variable to keep track of how many times the loop has run 

    for (int i = 10; i >= 1; i--) {
        std::cout << i << std::endl;
    }
    std::cout << "HAPPY NEW YEAR!" << std::endl;

    return 0;
}